#6. calculadora de 30, 60, 90%

Numporcex = float(input("coloque el numero que desea sacar el 30%, 60% y 90%: "))

porcetajemulti30 = Numporcex * 30
porce30 = porcetajemulti30 / 100

porcetajemulti60 = Numporcex * 60
porce60 = porcetajemulti60 / 100

porcetajemulti90 = Numporcex * 90
porce90 = porcetajemulti90 / 100

print(f"el 30% {porce30}")
print(f"el 60% {porce60}")
print(f"el 90% {porce90}")